import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"

import { cn } from "@/lib/utils"

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-md text-sm font-medium transition-all disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg:not([class*='size-'])]:size-4 shrink-0 [&_svg]:shrink-0 outline-none focus-visible:ring-2 focus-visible:ring-primary-500 focus-visible:ring-offset-2 focus-visible:ring-offset-background-50 dark:focus-visible:ring-offset-background-50",
  {
    variants: {
      variant: {
        default:
          "bg-primary-500 text-white shadow hover:bg-primary-600 dark:bg-primary-500 dark:text-white dark:hover:bg-primary-600",
        destructive:
          "bg-red-600 text-white shadow hover:bg-red-700 dark:bg-red-600 dark:hover:bg-red-700",
        outline:
          "border border-background-300 bg-transparent text-text-700 hover:bg-background-100 dark:border-background-300 dark:text-text-700 dark:hover:bg-background-200",
        secondary:
          "bg-secondary-500 text-white shadow hover:bg-secondary-600 dark:bg-secondary-500 dark:text-white dark:hover:bg-secondary-600",
        ghost:
          "text-text-700 hover:bg-background-100 dark:text-text-700 dark:hover:bg-background-200",
        link: "text-primary-500 underline-offset-4 hover:underline dark:text-primary-500",
      },
      size: {
        default: "h-9 px-4 py-2 has-[>svg]:px-3",
        sm: "h-8 rounded-md gap-1.5 px-3 has-[>svg]:px-2.5",
        lg: "h-10 rounded-md px-6 has-[>svg]:px-4",
        icon: "size-9",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
)

function Button({
  className,
  variant,
  size,
  asChild = false,
  ...props
}: React.ComponentProps<"button"> &
  VariantProps<typeof buttonVariants> & {
    asChild?: boolean
  }) {
  const Comp = asChild ? Slot : "button"

  return (
    <Comp
      data-slot="button"
      className={cn(buttonVariants({ variant, size, className }))}
      {...props}
    />
  )
}

export { Button, buttonVariants }
